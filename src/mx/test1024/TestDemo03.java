package mx.test1024;

/**
 * Math类
 */
public class TestDemo03 {
    public static void main(String[] args) {
        System.out.println("返回整数="+Math.abs(5));
        System.out.println("返回整数="+Math.abs(-5));
        System.out.println("返回大于或等于参数的最小double值，等于一个整数="+Math.ceil(5.4));
        System.out.println("返回小于或等于参数的最大double值，等于一个整数="+Math.floor(5.5));
        System.out.println("四舍五入返回最接近参数的int="+Math.round(5.5));
        System.out.println("四舍五入返回最接近参数的int="+Math.round(5.4));
    }

}
