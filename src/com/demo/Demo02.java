package com.demo;

/**
 * 常量 其值是不会发生改变的量 就是常量
 */
public class Demo02 {
    public static void main(String[] args) {

        //整数常量
        int i0=90;
        System.out.println(i0);
        //小数常量
        double d0=44.44;
        System.out.println(d0);
        //布尔常量
        System.out.println(true);
        //字符常量
        System.out.println('a');
        //字符串常量
        System.out.println("aaa");
    }

}
