package com.demo2;

/**
 * 关系运算符
 */
public class Demo05 {
    public static void main(String[] args) {
        //> >= < <= == !=
        System.out.println(1>3);
        System.out.println(1<3);
        System.out.println(1>=3);
        System.out.println(1<=3);
        System.out.println(1==3);
        System.out.println(1!=3);
    }
}
